    private void BTNCPineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNCPineActionPerformed
        // TODO add your handling code here:
        int total = Integer.parseInt(LBLQPine.getText());//turns quantity into integer
        total--;//subtracts one from total
        PineC += 1;//add one to cart
        LBLQPine.setText(String.valueOf(total));//set label to next number
        pricet = pricet + Double.parseDouble(LBLPPine.getText()); //adds to price
        LBLCPine.setText(String.valueOf(PineC)); //set label to next number
    }//GEN-LAST:event_BTNCPineActionPerformed

