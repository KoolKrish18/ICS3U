    public static void Outputter(int count, JLabel outP, JLabel outQ){
        final int TOTAL = 7; // final vairbale for bnumber of items
        int quantity [] = new int [TOTAL]; //make string array for quantity
        double total [] = new double [TOTAL]; //make string array for pricey
        try{
            FileReader file = new FileReader("Fruits.txt"); //get file for text
            
            BufferedReader buffer = new BufferedReader(file); //buffered reader (checks for spaces)
            for(int i = 0; i < TOTAL; i++){ //for loop
                quantity[i] = Integer.parseInt(buffer.readLine()); //puts first valeu into array for quanityt
                total[i] = Double.parseDouble(buffer.readLine()); //puts second value into array for price
            }
            buffer.close(); //ends the buffer
        }
        catch(IOException e){ //catch statement for exceptions
        }
        outQ.setText(String.valueOf(quantity[count-1])); //sets the label for quantity
        outP.setText(String.valueOf(total[count-1])); //sets the label for total
    }
    
